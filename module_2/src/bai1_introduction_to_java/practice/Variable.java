package bai1_introduction_to_java.practice;

public class Variable {
    public static void main(String[] args) {
                int i = 10; // khai báo Biến i kiểu số nguyên, có giá trị là 10
                float f = 20.5f; // Khai báo Biến f kiểu số thực, có giá trị là 20.5
                double d = 20.5; // Khai báo Biến d kiểu số thực lớn (double), có giá trị là 20.
                boolean b = true; // Khai báo Biến b kiểu logic, có giá trị là true
                char c = 'a'; // Khai báo Biến c kiểu ký tự, có giá trị là 'a'
                String s = "Hà Nội"; //Khai báo biến s có kiểu là String có giá trị là chuỗi Hà Nội
                System.out.println("i = " + i);
                System.out.println("f = " + f);
                System.out.println("d = " + d);
                System.out.println("b = " + b);
                System.out.println("c = " + c);
                System.out.println("s = " + s);

            }
        }


